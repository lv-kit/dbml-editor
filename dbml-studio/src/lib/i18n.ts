export function resolveLocale(storedValue: string | null, browserLanguage: string): Locale {
	if (isLocale(storedValue)) return storedValue;

	const language = browserLanguage.toLowerCase().split('-')[0];
	return isLocale(language) ? language : 'ja';
}

export function isLocale(value: string | null): value is Locale {
	return typeof value === 'string' && LOCALES.includes(value as Locale);
}

export function applyLocale(root: { lang: string }, locale: Locale) {
	root.lang = locale;
}
